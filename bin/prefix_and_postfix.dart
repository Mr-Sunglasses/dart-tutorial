void main(){

  // prefix Increment
  var i = 3;
  print(i);
  print(++i);
  print(i);

  print("------------------------------------");
  // postfix Increment
  var j = 4;
  print(j);
  print(j++);
  print(j);


  print("--------------------------------------");
  // Finally shown both prefix and postfix increment and decrement
  var k = 20;
  print(++k);
  print(--k);
  print(k++);
  print(k--);
  print(k);

}